fun main(){
    //Add two numbers
    fun addTwo(){
        val num1: Int=40
        val num2: Int=60
        val sum: Int=num1 + num2
        println("The sum is $sum")
    }
    //call the function
    addTwo()

    fun subTwo(){
        val num1: Int=40
        val num2: Int=60
        val difference: Int=num2 - num1
        println("The difference is $difference")
    }
    subTwo()

    fun multiplyTwo(){
        val num1: Int=40
        val num2: Int=60
        val product: Int=num1 * num2
        println("The product is $product")
    }
    multiplyTwo()

    fun divideTwo(){
        val num1: Int=540
        val num2: Int=60
        val division: Int=num1 / num2
        println("The division is $division")
    }
    divideTwo()
}